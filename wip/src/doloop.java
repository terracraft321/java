public class doloop { // initial class + opening bracket
    public static void main(String[] args) { // main
        int i = 0; // initialize variable
        do { // do keyword opening bracket
            i++; // iterate i
            System.out.println(i); // print i
        } // closing bracket, optional
        while (i < 100); // while keyword + loop-continuation-statement (logical condition)
        } // another closing bracket
    } // closing bracket for initial class
