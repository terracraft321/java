public class Exponent {
    public static void main(String[] args) {
        // Calculate remainders using the % operator
        int x1 = 56 % 6;
        int x2 = 78 % -4;
        int x3 = -34 % 5;
        int x4 = -34 % -5;
        int x5 = 5 % 1;
        int x6 = 1 % 5;

        // Print the results
        System.out.printf("x1 = %d\nx2 = %d\nx3 = %d\nx4 = %d\nx5 = %d\nx6 = %d\n", x1, x2, x3, x4, x5, x6);
    }
}
